package myFirstjavalearning;

public class Arithmetic {

	public static void main(String[] args) {
		int a = 25 , b = 5, c,d,e,f,g,h;
		
		c = a + b ;
		d =  a -b ;
		e = a/b ;
		f = a*b ;
		g = ++a ;
		h = --b ;
		System.out.println("Addition :" +c);
		System.out.println("Subraction:" +d);
		System.out.println("Division :" +e);
		System.out.println("Multiplcation :" +f);
		System.out.println("Increment :" + g);
		System.out.println("Decrement :" +h);
	}

}
